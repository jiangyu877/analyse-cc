CREATE TABLE ml.model_registry (
    model_id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    model_key      VARCHAR(40) NOT NULL,
    model_version  VARCHAR(32) NOT NULL,
    algorithm      VARCHAR(80) NOT NULL,
    feature_list   JSONB NOT NULL DEFAULT '[]'::jsonb
                   CHECK (jsonb_typeof(feature_list) = 'array'),
    metadata       JSONB NOT NULL DEFAULT '{}'::jsonb
                   CHECK (jsonb_typeof(metadata) = 'object'),
    created_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (model_key, model_version)
);


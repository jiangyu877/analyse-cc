ALTER TABLE ml.model_task
    ADD COLUMN model_id BIGINT REFERENCES ml.model_registry(model_id),
    ADD COLUMN dataset_snapshot JSONB NOT NULL DEFAULT '{}'::jsonb
               CHECK (jsonb_typeof(dataset_snapshot) = 'object'),
    ADD COLUMN code_version VARCHAR(64) NOT NULL DEFAULT 'pre-release-c';

UPDATE ml.model_task task
SET model_id = registry.model_id
FROM ml.model_registry registry
WHERE registry.model_key = task.task_type
  AND registry.model_version = 'baseline-v1'
  AND task.model_id IS NULL;


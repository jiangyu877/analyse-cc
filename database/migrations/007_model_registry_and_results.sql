CREATE INDEX idx_model_task_model_time
    ON ml.model_task(model_id, started_at DESC)
    WHERE model_id IS NOT NULL;

CREATE TABLE ml.customer_amount_prediction (
    task_id          BIGINT NOT NULL REFERENCES ml.model_task(task_id) ON DELETE CASCADE,
    customer_id      BIGINT NOT NULL REFERENCES biz.customer(customer_id),
    horizon_days     SMALLINT NOT NULL CHECK (horizon_days BETWEEN 1 AND 90),
    forecast_start   DATE NOT NULL,
    forecast_end     DATE NOT NULL,
    predicted_amount NUMERIC(18,2) NOT NULL CHECK (predicted_amount >= 0),
    created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (task_id, customer_id),
    CHECK (forecast_end >= forecast_start)
);

CREATE TABLE ml.product_sales_forecast (
    task_id           BIGINT NOT NULL REFERENCES ml.model_task(task_id) ON DELETE CASCADE,
    product_id        BIGINT NOT NULL REFERENCES biz.product(product_id),
    forecast_date     DATE NOT NULL,
    predicted_quantity NUMERIC(14,4) NOT NULL CHECK (predicted_quantity >= 0),
    created_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (task_id, product_id, forecast_date)
);

CREATE TABLE ml.product_recommendation (
    task_id       BIGINT NOT NULL REFERENCES ml.model_task(task_id) ON DELETE CASCADE,
    customer_id   BIGINT NOT NULL REFERENCES biz.customer(customer_id),
    product_id    BIGINT NOT NULL REFERENCES biz.product(product_id),
    rank_no       SMALLINT NOT NULL CHECK (rank_no > 0),
    score         NUMERIC(9,8) NOT NULL CHECK (score BETWEEN 0 AND 1),
    created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (task_id, customer_id, rank_no),
    UNIQUE (task_id, customer_id, product_id)
);

CREATE INDEX idx_customer_amount_customer_task
    ON ml.customer_amount_prediction(customer_id, task_id DESC);
CREATE INDEX idx_product_forecast_product_task
    ON ml.product_sales_forecast(product_id, task_id DESC, forecast_date);
CREATE INDEX idx_recommendation_customer_task
    ON ml.product_recommendation(customer_id, task_id DESC, rank_no);
